package package1;
public class Assignment3_Class {
	static void add()
	{
		int a = 100;
		int b = 15;
		int c = a + b;
		System.out.println(c);
	}
	static void sub()
	{
		int a = 100;
		int b = 15;
		int c = a - b;
		System.out.println(c);
	}
	static void multiply()
	{
		int a = 100;
		int b = 15;
		int c = a * b;
		System.out.println(c);
	}
	static void divide()
	{
		int a = 100;
		int b = 15;
		int c = a / b;
		System.out.println(c);
	}
	public static void main(String[] args) //Arithmetic Operator
	{
		add();
		sub();
		multiply();
		divide();
	}

}
